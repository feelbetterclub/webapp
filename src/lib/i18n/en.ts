type DeepStringify<T> = {
  [K in keyof T]: T[K] extends string
    ? string
    : T[K] extends readonly string[]
      ? string[]
      : T[K] extends readonly (infer U)[]
        ? DeepStringify<U>[]
        : DeepStringify<T[K]>;
};

